plt.ylabel("Thrust (N)", fontsize = 12)
plt.grid(True, linestyle = "--", alpha = 0.7)
plt.axhline(0, color = "black", linewidth = 0.1)
plt.xlim(left = 0)
plt.ylim(bottom = 0)
plt.legend()

#show graph
plt.show()